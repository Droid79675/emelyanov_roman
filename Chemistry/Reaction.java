public class Reaction {
    public int number;
    public String info;


    public Reaction(int number, String info){
        this.number = number;
        this.info = info;
    }
    public int getNumber(){
        return number;
    }
    public void setNumber(int number){
        this.number = number;
    }
    public String getInfo(){
        return info;
    }
    public void setInfo(String info){
        this.info = info;
    }
}
